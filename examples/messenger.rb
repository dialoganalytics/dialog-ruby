# This is an example integration of dialog-ruby with messenger
#
# Get started:
#   1. Create an account on https://app.dialoganalytics.com
#   2. Find your API token in your personal settings page and your Bot ID in the bot's settings page
#   3. Clone this repository and run `bundle install`
#   4. Run this example by typing `ruby examples/messenger.rb' from the repository root
#   5. Read more on how to make the most out of the possibilities offered by Dialog here: https://docs.dialoganalytics.com
